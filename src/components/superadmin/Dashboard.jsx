import React, { useState, useEffect } from "react";
import { useNavigate } from "react-router-dom";
import DataService from "../../services/axios";

// const Dashboard = () => {
function Dashboard(){
  const [admins, setAdmins] = useState([]);
  const navigate = useNavigate();

  useEffect(() => {
    DataService.get("/SuperAdmin/GetAdmins")
      .then((res) => setAdmins(res.data))
      .catch((err) => console.error(err));
  }, []);

  return (
    <div style={styles.container}>
      <div style={styles.header}>Admins</div>
      <div style={styles.cardContainer}>
        {admins.map((admin) => (
          <div key={admin.user_id} style={styles.listItem}>
            <span>{admin.name}</span>
            <button 
              style={styles.editBtn} 
              onClick={() => navigate(`/superadmin/edit-admins/${admin.user_id}/${admin.name}`)}
            >
              Edit
            </button>
          </div>
        ))}
      </div>
      <button style={styles.addBtn} onClick={() => navigate("/superadmin/add-admin")}>
        Add New Admin
      </button>
    </div>
  );
};

export default Dashboard;

